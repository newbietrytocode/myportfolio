print("=== โปรแกรมคำนวณพื้นที่ ===")
print("1. รูปสี่เหลี่ยมผืนผ้า")
print("2. รูปสามเหลี่ยม")
print("3. รูปวงกลม")
เมนู = input("กรุณาเลือกเมนู (1/2/3): ")

if เมนู == "1":
    กว้าง = float(input("ระบุความกว้าง: "))
    ยาว = float(input("ระบุความยาว: "))
    พื้นที่ = กว้าง * ยาว
    print("พื้นที่สี่เหลี่ยมคือ:", พื้นที่)

elif เมนู == "2":
    ฐาน = float(input("ระบุความยาวฐาน: "))
    สูง = float(input("ระบุความสูง: "))
    พื้นที่ = 0.5 * ฐาน * สูง
    print("พื้นที่สามเหลี่ยมคือ:", พื้นที่)

elif เมนู == "3":
    รัศมี = float(input("ระบุความยาวรัศมี (r): "))
    พื้นที่ = 3.14 * รัศมี * รัศมี
    print("พื้นที่วงกลมคือ:", พื้นที่)

else:
    print("คุณเลือกเมนูไม่ถูกต้อง!")